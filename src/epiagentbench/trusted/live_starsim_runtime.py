"""Evaluator-only closed-loop Starsim investigation runtime.

The runtime keeps two identically seeded biological worlds inside the trusted
process.  The active world receives the evaluated agent's controls; the shadow
world never does.  Reporting artifacts are generated by the surveillance
layer, not by Starsim, and use the same precommitted-candidate pattern as the
biological routes.
"""

