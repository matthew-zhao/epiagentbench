class ClosedLoopStarsimRuntime:
    """A live active world plus a hidden, no-action comparison world."""

    def __init__(
        self,
        *,
        seed: int,
        presentation_key: bytes,
        profile: Mapping[str, Any],
        config: StarsimSIRConfig,
        growth_regime: str,
        causal_mode: str = "person_to_person",
        family: str = "institution_person_to_person",
        initial_artifact_duplicates: int = 0,
        future_artifact_candidates: Iterable[tuple[int, int]] = (),
    ) -> None:
        if causal_mode not in CAUSAL_MODES:
            raise ValueError("Unsupported closed-loop causal mode")
        if not isinstance(family, str) or not family.strip():
            raise ValueError("family must be a non-empty string")
        if (
            type(initial_artifact_duplicates) is not int
            or initial_artifact_duplicates < 0
        ):
            raise ValueError("initial_artifact_duplicates must be non-negative")

        self._seed = seed
        self._presentation_key = presentation_key
        self._profile = dict(profile)
        self._config = config
        self._growth_regime = growth_regime
        self._causal_mode = causal_mode
        self._family = family
        transmission = self._profile["transmission_configuration"]
        closed_loop = self._profile["closed_loop_configuration"]
        self._decision_minute = int(transmission["decision_day"]) * DAY_MINUTES
        self._deadline_minutes = int(closed_loop["interaction_days"]) * DAY_MINUTES
        self._tick_minutes = int(closed_loop["implementation_cycle_minutes"])
        self._future_artifact_candidates = self._normalize_artifact_candidates(
            future_artifact_candidates
        )
        utility_profile = self._profile.get("utility_profile", {})
        if not isinstance(utility_profile, Mapping):
            raise ValueError("Invalid utility profile")
        artifact_weight = utility_profile.get("artifact_record_weight", 0.5)
        if (
            type(artifact_weight) not in (int, float)
            or not math.isfinite(float(artifact_weight))
            or float(artifact_weight) < 0
        ):
            raise ValueError("Invalid reporting-artifact utility weight")
        self._artifact_record_weight = float(artifact_weight)

        forecast = closed_loop.get("forecast")
        if not isinstance(forecast, Mapping):
            raise ValueError("Closed-loop profile is missing forecast settings")
        self._forecast_horizon_minutes = forecast.get("horizon_minutes")
        self._forecast_minimum_submissions = forecast.get("minimum_submissions")
        if (
            forecast.get("target") != "new_encounters"
            or forecast.get("scoring_rule")
            != "symmetric_log_gaussian_base_2"
            or type(self._forecast_horizon_minutes) is not int
            or not 1
            <= self._forecast_horizon_minutes
            <= self._deadline_minutes
            or type(self._forecast_minimum_submissions) is not int
            or self._forecast_minimum_submissions < 1
            or type(forecast.get("review_minutes")) is not int
            or forecast["review_minutes"] < self._tick_minutes
            or forecast["review_minutes"] % self._tick_minutes != 0
            or forecast["review_minutes"] > self._deadline_minutes
        ):
            raise ValueError("Invalid closed-loop forecast settings")
        if self._tick_minutes != config.timestep_minutes:
            raise ValueError(
                "The public intervention cycle must match this experimental "
                "Starsim adapter's fixed step"
            )

        # Validate every level before simulator construction.  This keeps a bad
        # trusted profile from failing only after an evaluated action occurs.
        for action_type in RESPONSE_ACTION_TYPES:
            for level in LEVELS:
                self._level_values(level, action_type)

        self._active = StarsimDiseaseEngine(config)
        self._shadow = StarsimDiseaseEngine(config)
        self._closed = False
        self._final_oracle: Oracle | None = None
        self._current_public_minute = 0
        self._requested_levels = {
            action_type: "off" for action_type in RESPONSE_ACTION_TYPES
        }
        self._control_changes: dict[str, list[tuple[int, str]]] = {
            action_type: [] for action_type in RESPONSE_ACTION_TYPES
        }
        # Retain private compatibility fields used by the original P2P tests.
        self._requested_level = "off"
        self._level_changes = self._control_changes["infection_control"]
        self._intervention_sequence = 0
        self._first_biological_effective_minute: int | None = None
        self._first_effective_minute: int | None = None

        try:
            self._active.advance_to(self._decision_minute)
            self._shadow.advance_to(self._decision_minute)
            active_snapshot = self._active.oracle_snapshot()
            shadow_snapshot = self._shadow.oracle_snapshot()
            if (
                active_snapshot.transmission_events
                != shadow_snapshot.transmission_events
            ):
                raise RuntimeError("No-action Starsim twins diverged at bootstrap")

            self._stream = IncrementalSurveillanceStream(
                seed=seed,
                presentation_key=presentation_key,
                profile=self._profile,
                population_size=active_snapshot.population_size,
                decision_minute=self._decision_minute,
                deadline_minutes=self._deadline_minutes,
                review_interval_minutes=self._tick_minutes,
                causal_mode=causal_mode,
                artifact_duplicate_count=initial_artifact_duplicates,
            )
            self._stream.ingest(active_snapshot.transmission_events)
            self._public_episode = self._stream.bootstrap()
            self._infections_at_decision = len(active_snapshot.transmission_events)
            self._secondary_at_decision = sum(
                event.source_agent_id is not None
                for event in active_snapshot.transmission_events
            )
            self._mechanism_counts_at_decision = {
                mechanism: sum(
                    event.mechanism == mechanism
                    for event in active_snapshot.transmission_events
                )
                for mechanism in (COMMON_SOURCE, IMPORTATION)
            }
        except Exception:
            self._active.close()
            self._shadow.close()
            self._closed = True
            raise

    @property
    def public_episode(self) -> PublicEpisode:
        return self._public_episode

    @property
    def canary_tokens(self) -> tuple[str, ...]:
        return self._stream.canary_tokens

    @property
    def initial_diagnostics(self):
        """Private generation diagnostics used only for observable alert gating."""

        return self._stream.initial_diagnostics

    @property
    def growth_regime(self) -> str:
        """Private generation stratum; never placed in a public response."""

        return self._growth_regime

    @property
    def causal_mode(self) -> str:
        """Private causal label; never placed in a public response."""

        return self._causal_mode

    def advance_to(self, public_minute: int) -> tuple[Observation, ...]:
        self._ensure_open()
        if type(public_minute) is not int or not (
            self._current_public_minute
            <= public_minute
            <= self._deadline_minutes
        ):
            raise ValueError("Invalid closed-loop public time")

        absolute_target = self._decision_minute + public_minute
        engine_target = absolute_target // self._tick_minutes * self._tick_minutes
        if engine_target > self._active.current_minute:
            self._active.advance_to(engine_target)
            self._shadow.advance_to(engine_target)

        active_events = self._active.oracle_snapshot().transmission_events
        shadow_events = self._shadow.oracle_snapshot().transmission_events
        if (
            self._first_biological_effective_minute is None
            or engine_target <= self._first_biological_effective_minute
        ) and active_events != shadow_events:
            raise RuntimeError("Closed-loop worlds diverged before control effect")

        observations = list(self._stream.ingest(active_events))
        observations.extend(self._materialize_artifacts_through(public_minute))
        self._current_public_minute = public_minute
        return self._ordered_observations(observations)

    def apply_institution_control(
        self,
        level: str,
        target_id: str | None,
        public_minute: int,
    ) -> RuntimeInterventionReceipt:
        """Compatibility alias for the generic infection-control action."""

        return self.apply_response_control(
            "infection_control", level, target_id, public_minute
        )

    def apply_response_control(
        self,
        action_type: str,
        level: str,
        target_id: str | None,
        public_minute: int,
    ) -> RuntimeInterventionReceipt:
        """Schedule one idempotent, route-specific response control."""

        self._ensure_open()
        if action_type not in RESPONSE_ACTION_TYPES:
            raise ValueError("Unknown response control action")
        if level not in LEVELS:
            raise ValueError("Unknown response control level")
        expected_target = self._stream.response_control_targets[action_type]
        if target_id != expected_target:
            raise ValueError("Control target does not match the response action")
        if public_minute != self._current_public_minute:
            raise ValueError("Control time does not match the live world")
        if level == self._requested_levels[action_type]:
            return RuntimeInterventionReceipt(
                status="no_change",
                intervention_id=None,
                effective_at_minute=None,
                level=level,
            )

        absolute_now = self._decision_minute + public_minute
        effective_minute = (
            absolute_now // self._tick_minutes + 1
        ) * self._tick_minutes
        effective_public_minute = effective_minute - self._decision_minute
        if effective_public_minute > self._deadline_minutes:
            raise ValueError("Control cannot take effect before the deadline")

        self._intervention_sequence += 1
        sequence = self._intervention_sequence
        control_kind = CONTROL_KINDS[action_type]
        if control_kind is not None:
            self._active.apply_control(
                EngineControl(
                    control_id=f"live_control_{sequence:06d}",
                    kind=control_kind,
                    effective_minute=effective_minute,
                    magnitude=float(
                        self._level_values(level, action_type)["effect_level"]
                    ),
                )
            )
            if self._first_biological_effective_minute is None:
                self._first_biological_effective_minute = effective_minute

        self._requested_levels[action_type] = level
        self._control_changes[action_type].append((effective_minute, level))
        if action_type == "infection_control":
            self._requested_level = level
        if self._first_effective_minute is None:
            self._first_effective_minute = effective_minute
        status_observation = self._stream.add_response_control_status(
            action_type=action_type,
            target_id=expected_target,
            sequence=sequence,
            level=level,
            effective_public_minute=effective_public_minute,
        )
        return RuntimeInterventionReceipt(
            status="scheduled",
            intervention_id=self._intervention_id(sequence),
            effective_at_minute=effective_public_minute,
            level=level,
            observations=(status_observation,),
        )

    def finalize(self) -> Oracle:
        if self._final_oracle is not None:
            return self._final_oracle
        self._ensure_open()

        # Fixed endpoints prevent early submission from hiding adverse outcomes.
        self._advance_for_scoring(
            self._decision_minute + self._deadline_minutes,
            materialize_observations=True,
        )
        self._advance_for_scoring(
            self._config.horizon_days * DAY_MINUTES,
            materialize_observations=False,
        )
        active_snapshot = self._active.oracle_snapshot()
        shadow_snapshot = self._shadow.oracle_snapshot()
        active_infections = len(active_snapshot.transmission_events)
        shadow_infections = len(shadow_snapshot.transmission_events)
        active_artifacts = self._stream.total_emitted_reporting_artifacts
        shadow_artifacts = len(self._future_artifact_candidates)
        burden = self._total_trajectory_burden(self._control_changes)
        realized_utility = (
            shadow_infections
            - active_infections
            + (shadow_artifacts - active_artifacts)
            * self._artifact_record_weight
            - burden
        )

        fixed_utilities = self._fixed_bundle_utilities(
            no_action_infections=shadow_infections,
            no_action_artifacts=shadow_artifacts,
        )
        best_bundle, best_fixed = max(
            fixed_utilities.items(), key=lambda item: item[1]
        )
        policy_floor = min(fixed_utilities.values())
        targets = self._stream.response_control_targets
        single_action_best = {
            action_type: max(
                fixed_utilities[
                    tuple(
                        level if name == action_type else "off"
                        for name in RESPONSE_ACTION_TYPES
                    )
                ]
                for level in LEVELS
            )
            for action_type in RESPONSE_ACTION_TYPES
        }

        biological_mode = self._causal_mode in BIOLOGICAL_MODES
        sufficient_revealed_cases = (
            len(self._stream.investigation_true_case_ids) >= 3
        )
        mode_cluster_size = {
            "person_to_person": self._secondary_at_decision,
            "common_source": self._mechanism_counts_at_decision[COMMON_SOURCE],
            "repeated_introduction": self._mechanism_counts_at_decision[
                IMPORTATION
            ],
            "background": 0,
            "reporting_artifact": 0,
        }[self._causal_mode]
        is_outbreak = (
            biological_mode
            and sufficient_revealed_cases
            and mode_cluster_size >= 3
        )
        explanation_type, source_kind = MODE_ORACLE_LABELS[self._causal_mode]
        # The P2P generator can still yield a coherent false alert.  In that
        # case the agent should not be rewarded for parroting a latent route
        # label when the frozen evidence does not establish propagated spread.
        if self._causal_mode == "person_to_person" and not is_outbreak:
            explanation_type, source_kind = ("sporadic_background", None)
        source_id = {
            "institution": self._stream.institution_id,
            "food_service": self._stream.food_service_id,
            "entry_program": self._stream.entry_program_id,
            "reporting_system": self._stream.reporting_system_id,
            None: None,
        }[source_kind]
        decisive_ids = self._oracle_decisive_evidence_ids()

        legacy_fixed = {
            level: fixed_utilities[
                tuple(
                    level if action == "infection_control" else "off"
                    for action in RESPONSE_ACTION_TYPES
                )
            ]
            for level in LEVELS
        }
        uniform_fixed = {
            level: fixed_utilities[tuple(level for _ in RESPONSE_ACTION_TYPES)]
            for level in LEVELS
        }
        metrics: dict[str, float | int | str] = {
            "response_utility_profile_id": str(self._profile["profile_id"]),
            "response_utility_timing_model": "closed_loop_realized_trajectory",
            "counterfactual_no_action_infections": shadow_infections,
            "realized_active_infections": active_infections,
            "counterfactual_infections_averted": (
                shadow_infections - active_infections
            ),
            "counterfactual_no_action_artifact_emissions": shadow_artifacts,
            "realized_active_artifact_emissions": active_artifacts,
            "counterfactual_artifact_emissions_prevented": (
                shadow_artifacts - active_artifacts
            ),
            "artifact_record_weight": self._artifact_record_weight,
            "intervention_burden": round(burden, 6),
            "realized_intervention_utility": round(realized_utility, 6),
            "closed_loop_best_fixed_utility": round(best_fixed, 6),
            "closed_loop_policy_floor": round(policy_floor, 6),
            "closed_loop_fixed_off_utility": round(legacy_fixed["off"], 6),
            "closed_loop_fixed_standard_utility": round(
                legacy_fixed["standard"], 6
            ),
            "closed_loop_fixed_intensive_utility": round(
                legacy_fixed["intensive"], 6
            ),
            "closed_loop_uniform_standard_utility": round(
                uniform_fixed["standard"], 6
            ),
            "closed_loop_uniform_intensive_utility": round(
                uniform_fixed["intensive"], 6
            ),
            "best_fixed_response_action": self._bundle_action_label(best_bundle),
            "best_fixed_response_bundle": self._bundle_string(best_bundle),
            "intervention_state_changes": sum(
                len(changes) for changes in self._control_changes.values()
            ),
            "investigation_gold_cutoff_minute": 0,
            "outcome_horizon_days": self._config.horizon_days,
            "forecast_scoring_rule": "symmetric_log_gaussian_base_2",
        }
        for action_type in RESPONSE_ACTION_TYPES:
            metrics[f"{action_type}_state_changes"] = len(
                self._control_changes[action_type]
            )
            for level in LEVELS:
                bundle = tuple(
                    level if action == action_type else "off"
                    for action in RESPONSE_ACTION_TYPES
                )
                metrics[
                    f"closed_loop_fixed_{action_type}_{level}_utility"
                ] = round(fixed_utilities[bundle], 6)

        self._final_oracle = Oracle(
            family=f"{self._family}_closed_loop",
            is_outbreak=is_outbreak,
            # The immutable decision-point target cannot be shrunk by controls.
            true_case_ids=self._stream.investigation_true_case_ids,
            explanation_type=explanation_type,
            source_id=source_id,
            decisive_evidence_ids=decisive_ids,
            action_utilities={
                ("monitor", None): 0.0,
                **{
                    (action_type, targets[action_type]): single_action_best[
                        action_type
                    ]
                    for action_type in RESPONSE_ACTION_TYPES
                },
            },
            canary_tokens=self._stream.canary_tokens,
            counterfactual_metrics=metrics,
            forecast_event_minutes=tuple(
                sorted(
                    observation.available_minute
                    for observation in self._stream.all_observations
                    if observation.kind == "encounter"
                    and observation.available_minute > 0
                )
            ),
            forecast_horizon_minutes=self._forecast_horizon_minutes,
            forecast_minimum_submissions=self._forecast_minimum_submissions,
            followup_true_case_observation_ids=(
                self._stream.followup_true_case_observation_ids
            ),
            followup_relevant_evidence_ids=(
                self._stream.followup_relevant_evidence_ids
            ),
        )
        self._active.close()
        self._shadow.close()
        self._closed = True
        return self._final_oracle

    def close(self) -> None:
        if self._closed:
            return
        self._active.close()
        self._shadow.close()
        self._closed = True

    def _advance_for_scoring(
        self, absolute_target: int, *, materialize_observations: bool
    ) -> None:
        if absolute_target > self._active.current_minute:
            self._active.advance_to(absolute_target)
            self._shadow.advance_to(absolute_target)
        if materialize_observations:
            self._stream.ingest(self._active.oracle_snapshot().transmission_events)
            self._materialize_artifacts_through(self._deadline_minutes)

    def _fixed_bundle_utilities(
        self,
        *,
        no_action_infections: int,
        no_action_artifacts: int,
    ) -> dict[tuple[str, ...], float]:
        """Score all 3^4 bundles while simulating only causal dimensions.

        Irrelevant controls have no simulator effect and non-negative additive
        burden.  Collapsing those biological runs is therefore exactly
        equivalent to launching 81 duplicate Starsim worlds.
        """

        relevant = tuple(
            action
            for action in MODE_RELEVANT_ACTIONS[self._causal_mode]
            if action != "audit_reporting"
        )
        infections_by_relevant_levels: dict[tuple[str, ...], int] = {
            tuple("off" for _ in relevant): no_action_infections
        }
        for relevant_levels in itertools.product(LEVELS, repeat=len(relevant)):
            if relevant_levels in infections_by_relevant_levels:
                continue
            engine = StarsimDiseaseEngine(self._config)
            effective_minute = self._decision_minute + self._tick_minutes
            try:
                for index, (action_type, level) in enumerate(
                    zip(relevant, relevant_levels, strict=True), start=1
                ):
                    if level == "off":
                        continue
                    control_kind = CONTROL_KINDS[action_type]
                    if control_kind is None:  # pragma: no cover - defensive
                        raise RuntimeError("Audit control reached a Starsim world")
                    engine.apply_control(
                        EngineControl(
                            control_id=f"fixed_{index}_{action_type}_{level}",
                            kind=control_kind,
                            effective_minute=effective_minute,
                            magnitude=float(
                                self._level_values(level, action_type)[
                                    "effect_level"
                                ]
                            ),
                        )
                    )
                engine.advance_to(self._config.horizon_days * DAY_MINUTES)
                infections_by_relevant_levels[relevant_levels] = len(
                    engine.oracle_snapshot().transmission_events
                )
            finally:
                engine.close()

        utilities: dict[tuple[str, ...], float] = {}
        effective_minute = self._decision_minute + self._tick_minutes
        for bundle in itertools.product(LEVELS, repeat=len(RESPONSE_ACTION_TYPES)):
            level_by_action = dict(zip(RESPONSE_ACTION_TYPES, bundle, strict=True))
            relevant_levels = tuple(level_by_action[action] for action in relevant)
            infections = infections_by_relevant_levels[relevant_levels]
            audit_level = level_by_action["audit_reporting"]
            artifact_emissions = self._fixed_artifact_emissions(audit_level)
            burden = sum(
                self._trajectory_burden(
                    ((effective_minute, level_by_action[action_type]),),
                    action_type,
                )
                for action_type in RESPONSE_ACTION_TYPES
            )
            utilities[bundle] = (
                no_action_infections
                - infections
                + (no_action_artifacts - artifact_emissions)
                * self._artifact_record_weight
                - burden
            )
        return utilities

    def _fixed_artifact_emissions(self, audit_level: str) -> int:
        residual = float(
            self._level_values(audit_level, "audit_reporting")["effect_level"]
        )
        return sum(
            threshold_ppm / 1_000_000 < residual
            for _, threshold_ppm in self._future_artifact_candidates
        )

    def _total_trajectory_burden(
        self, changes_by_action: Mapping[str, Iterable[tuple[int, str]]]
    ) -> float:
        return sum(
            self._trajectory_burden(changes_by_action[action_type], action_type)
            for action_type in RESPONSE_ACTION_TYPES
        )

    def _trajectory_burden(
        self,
        changes: Iterable[tuple[int, str]],
        action_type: str = "infection_control",
    ) -> float:
        horizon = self._config.horizon_days * DAY_MINUTES
        cursor = self._decision_minute
        current_level = "off"
        burden = 0.0
        # Stable sorting preserves the trusted sequence for controls scheduled
        # at the same implementation boundary.
        for minute, level in sorted(changes, key=lambda item: item[0]):
            if minute > horizon:
                break
            burden += (
                max(0, minute - cursor)
                / DAY_MINUTES
                * float(
                    self._level_values(current_level, action_type)[
                        "burden_per_day"
                    ]
                )
            )
            cursor = max(cursor, minute)
            current_level = level
        burden += (
            max(0, horizon - cursor)
            / DAY_MINUTES
            * float(
                self._level_values(current_level, action_type)["burden_per_day"]
            )
        )
        return burden

    def _level_values(
        self, level: str, action_type: str = "infection_control"
    ) -> Mapping[str, float | int]:
        if action_type not in RESPONSE_ACTION_TYPES or level not in LEVELS:
            raise ValueError("Unknown closed-loop intervention level")
        closed_loop = self._profile["closed_loop_configuration"]
        response_controls = closed_loop.get("response_controls", {})
        raw_action = (
            response_controls.get(action_type, {})
            if isinstance(response_controls, Mapping)
            else {}
        )
        raw_levels = (
            raw_action.get("levels", raw_action)
            if isinstance(raw_action, Mapping)
            else {}
        )
        values = raw_levels.get(level, {}) if isinstance(raw_levels, Mapping) else {}
        if not isinstance(values, Mapping) or not values:
            if action_type == "infection_control":
                legacy = closed_loop["institution_control_levels"][level]
                values = {
                    "effect_level": legacy["transmission_level"],
                    "burden_per_day": legacy["burden_per_day"],
                    "setup_credits": legacy["setup_credits"],
                }
            else:
                values = _FALLBACK_RESPONSE_LEVELS[action_type][level]

        effect = values.get("effect_level", values.get("transmission_level"))
        burden = values.get("burden_per_day")
        setup_credits = values.get("setup_credits")
        if (
            type(effect) not in (int, float)
            or not math.isfinite(float(effect))
            or not 0 <= float(effect) <= 1
            or type(burden) not in (int, float)
            or not math.isfinite(float(burden))
            or float(burden) < 0
            or type(setup_credits) is not int
            or setup_credits < 0
        ):
            raise ValueError("Invalid closed-loop intervention profile")
        return {
            "effect_level": float(effect),
            "transmission_level": float(effect),
            "burden_per_day": float(burden),
            "setup_credits": setup_credits,
        }

    def _materialize_artifacts_through(
        self, through_public_minute: int
    ) -> tuple[Observation, ...]:
        created: list[Observation] = []
        for candidate in self._future_artifact_candidates:
            release_minute = candidate[0]
            if release_minute > through_public_minute:
                break
            audit_level = self._control_level_at_public_minute(
                "audit_reporting", release_minute
            )
            residual = float(
                self._level_values(audit_level, "audit_reporting")["effect_level"]
            )
            created.extend(
                self._stream.materialize_reporting_artifact_candidates(
                    (candidate,),
                    through_public_minute=release_minute,
                    audit_level=residual,
                )
            )
        return self._ordered_observations(created)

    def _control_level_at_public_minute(
        self, action_type: str, public_minute: int
    ) -> str:
        absolute_minute = self._decision_minute + public_minute
        level = "off"
        for effective_minute, candidate_level in sorted(
            self._control_changes[action_type], key=lambda item: item[0]
        ):
            if effective_minute > absolute_minute:
                break
            level = candidate_level
        return level

    def _oracle_decisive_evidence_ids(self) -> frozenset[str]:
        if self._causal_mode in BIOLOGICAL_MODES:
            return self._stream.investigation_decisive_evidence_ids
        observations = self._stream.all_observations
        if self._causal_mode == "background":
            return frozenset(
                observation.observation_id
                for observation in observations
                if observation.kind == "inspection"
                and observation.available_minute <= self._deadline_minutes
            )
        # Reporting artifacts are not people.  The stream freezes reachable
        # duplicate reports and the reporting-system inspection at bootstrap,
        # so later audit actions cannot shrink their evidence denominator.
        return self._stream.investigation_decisive_evidence_ids

    def _normalize_artifact_candidates(
        self, candidates: Iterable[tuple[int, int]]
    ) -> tuple[tuple[int, int], ...]:
        try:
            normalized = tuple(candidates)
        except TypeError as exc:
            raise TypeError("future_artifact_candidates must be iterable") from exc
        seen: set[tuple[int, int]] = set()
        for candidate in normalized:
            if (
                not isinstance(candidate, tuple)
                or len(candidate) != 2
                or type(candidate[0]) is not int
                or type(candidate[1]) is not int
                or not 0 <= candidate[0] <= self._deadline_minutes
                or not 0 <= candidate[1] < 1_000_000
            ):
                raise ValueError("Invalid future reporting-artifact candidate")
            if candidate in seen:
                raise ValueError("Duplicate future reporting-artifact candidate")
            seen.add(candidate)
        return tuple(sorted(normalized))

    @staticmethod
    def _ordered_observations(
        observations: Iterable[Observation],
    ) -> tuple[Observation, ...]:
        unique = {observation.observation_id: observation for observation in observations}
        return tuple(
            sorted(
                unique.values(),
                key=lambda observation: (
                    observation.available_minute,
                    observation.observation_id,
                ),
            )
        )

    @staticmethod
    def _bundle_action_label(bundle: tuple[str, ...]) -> str:
        active = [
            action
            for action, level in zip(RESPONSE_ACTION_TYPES, bundle, strict=True)
            if level != "off"
        ]
        if not active:
            return "off"
        if len(active) == 1:
            return active[0]
        return "combined"

    @staticmethod
    def _bundle_string(bundle: tuple[str, ...]) -> str:
        return ";".join(
            f"{action}={level}"
            for action, level in zip(RESPONSE_ACTION_TYPES, bundle, strict=True)
        )

    def _intervention_id(self, sequence: int) -> str:
        digest = hmac.new(
            self._presentation_key,
            f"epiagentbench:live:v1:intervention:{sequence}".encode("ascii"),
            hashlib.sha256,
        ).hexdigest()[:16]
        return f"intv_{digest}"

    def _ensure_open(self) -> None:
        if self._closed:
            raise RuntimeError("Closed-loop runtime is closed")


def trace_prefix(
    events: tuple[TransmissionEvent, ...], minute: int
) -> tuple[TransmissionEvent, ...]:
    """Return a deterministic transmission prefix for invariant tests."""

    return tuple(event for event in events if event.infection_minute < minute)
